// Regenerates client/src/constants/videoUrls.js from
// client/scripts/cloudinary-urls.json (the {local_path: cloudinary_url}
// mapping produced by upload-to-cloudinary.js).
//
// Usage: node client/scripts/generate-video-urls.js

import { fileURLToPath } from "node:url";
import path from "node:path";
import fs from "node:fs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const MAPPING_PATH = path.join(__dirname, "cloudinary-urls.json");
const OUT_PATH = path.join(__dirname, "..", "src", "constants", "videoUrls.js");

const mapping = JSON.parse(fs.readFileSync(MAPPING_PATH, "utf8"));
const entries = Object.entries(mapping).sort(([a], [b]) => a.localeCompare(b));

const missing = entries.filter(([, url]) => !url || !url.startsWith("http"));
if (missing.length) {
  console.error(`Refusing to generate: ${missing.length} entries have no real URL:`);
  missing.forEach(([k]) => console.error(`  - ${k}`));
  process.exit(1);
}

const body = entries.map(([localPath, url]) => `  ${JSON.stringify(localPath)}: ${JSON.stringify(url)},`).join("\n");

const fileContent = `// AUTO-GENERATED by client/scripts/generate-video-urls.js — do not edit by hand.
// Source: client/scripts/cloudinary-urls.json (produced by upload-to-cloudinary.js).
// Regenerate after any change to that mapping: node client/scripts/generate-video-urls.js
//
// Maps the local dev path a video used to be served from (still the shape
// stored in MongoDB formation documents) to its Cloudinary URL, so the app
// can resolve either a legacy local path or a Cloudinary URL to the right
// place to actually play the video from in production.
export const VIDEO_URLS = {
${body}
};

// formation.videoUrl / trailerVideoUrl fields may already contain a full URL
// (YouTube links, or a Cloudinary URL after the DB migration) — only route
// through the mapping for the old "/videos-..." local-path shape.
export function resolveVideoUrl(localPathOrUrl) {
  if (!localPathOrUrl) return localPathOrUrl;
  return VIDEO_URLS[localPathOrUrl] || localPathOrUrl;
}
`;

fs.mkdirSync(path.dirname(OUT_PATH), { recursive: true });
fs.writeFileSync(OUT_PATH, fileContent);
console.log(`${entries.length} entrées écrites dans ${OUT_PATH}`);
